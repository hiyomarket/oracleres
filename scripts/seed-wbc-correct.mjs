// 輔助函數：將本地時間轉換為 UTC 毫秒時間戳
function toUTC(year, month, day, hour, minute, offsetHours) {
  // offsetHours: 本地時區相對 UTC 的偏移（如 UTC+9 = 9，UTC-4 = -4）
  const localMs = Date.UTC(year, month - 1, day, hour, minute, 0, 0);
  return localMs - offsetHours * 60 * 60 * 1000;
}

