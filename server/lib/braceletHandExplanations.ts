/**
 * 取得左右手佩戴手串的能量說明（左進右出原則）
 */
export function getBraceletHandExplanation(
  element: string,
  hand: "left" | "right"
): { title: string; purpose: string; benefits: string[]; principle: string } {
  const data = LEFT_RIGHT_EXPLANATIONS[element];
  const handData = data?.[hand];
  const principle = hand === "left"
    ? "左手為能量接收端（吸納手），主要用於吸收、引入、增強正向能量。當你希望從水晶或外界獲得某些特質時，應佩戴在左手。"
    : "右手為能量釋放端（放射手），主要用於釋放、排出、抗禦負向能量。當你希望清除自身負能量或保護自己免受外界干擾時，應佩戴在右手。";
  if (!handData) {
    return {
      title: `${hand === "left" ? "左" : "右"}手佩戴${element}系手串`,
      purpose: hand === "left" ? "吸納補益喜用神能量" : "釋放負能量形成防護層",
      benefits: [],
      principle,
    };
  }
  return { ...handData, principle };
}

